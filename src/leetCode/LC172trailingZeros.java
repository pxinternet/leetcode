package leetCode;

/**
 * LC172 - 阶乘后的零
 *
 * 题目（概要）：给定整数 n，返回 n! 结果中尾随零的数量。
 * 尾随零由因子 10 = 2×5 产生，其中 2 的个数总不少于 5，故只需统计因子 5 的个数。
 *
 * 解法说明：
 * - 核心思想：统计 1 到 n 中因子 5 的个数。每 5 个数有 1 个 5，每 25 个再多 1 个，依此类推。
 * - 等价于 n/5 + n/25 + n/125 + ...，可用循环 n/=5; count+=n 实现。
 *
 * 时间复杂度：O(log_5 n)
 * 空间复杂度：O(1)
 *
 * 边界与注意事项：
 * - n=0 时 0!=1，尾随零为 0
 *
 * 示例：n=5 → 1（5!=120）；n=25 → 6
 */
public class LC172trailingZeros {

    /**
     * 统计 n! 中尾随零的个数（即因子 5 的个数）
     *
     * 关键点：n/5 为 5 的倍数个数，n/25 为 25 的倍数（多一个 5），依此类推
     *
     * @param n 非负整数
     * @return 尾随零个数
     */
    public int trailingZeroes(int n) {
        int count = 0;
        while (n > 0) {
            n /= 5;         // 下一轮统计 25、125、625... 的贡献
            count += n;     // 当前轮：n 个 5 的倍数各贡献至少 1 个因子 5
        }
        return count;
    }
}
