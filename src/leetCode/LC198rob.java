package leetCode;

/**
 * LC198rob - 打家劫舍
 *
 * 题目（概要）：给定数组 nums，nums[i] 表示第 i 间房屋的金额。不能偷相邻的两间房屋，求能偷到的最大金额。
 *
 * 算法原理：
 * - 最优子结构：偷前 i 间的最大收益，要么包含第 i 间（则必不偷 i-1，从 dp[i-2] 转移），要么不包含第 i 间（从 dp[i-1] 转移）。
 * - 无后效性：dp[i] 只依赖 dp[i-1]、dp[i-2]，与更早决策无关。
 * - 设计动机：每间房「偷或不偷」二选一，相邻约束导致状态只依赖前两格。
 *
 * 核心逻辑（分步）：
 * - 步骤 1：dp[0]=0（无房），dp[1]=nums[0]（只偷第一间）。
 * - 步骤 2：对 i>=2，dp[i] = max(不偷第 i 间：dp[i-1], 偷第 i 间：dp[i-2]+nums[i-1])。
 * - 步骤 3：答案为 dp[n]。
 *
 * 关键洞察：
 * - 递推公式的决策含义：max 的两项分别对应「跳过当前房」「偷当前房」两种策略。
 * - 空数组、单元素需单独处理；可滚动变量优化为 O(1) 空间。
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(n)，可优化为 O(1) 滚动变量
 *
 * 示例：nums = [2,7,9,3,1] → 12（偷 1、3、5 号）
 */
public class LC198rob {

    /**
     * 计算不偷相邻房屋时的最大金额
     *
     * 关键点逐行说明：
     * - dp[i] 表示考虑前 i 间房屋（索引 0 到 i-1）能偷到的最大金额
     * - dp[0]=0：没有房屋可偷；dp[1]=nums[0]：只偷第 1 间
     * - dp[i] = max(不偷第 i 间则 dp[i-1]，偷第 i 间则 dp[i-2]+nums[i-1])
     *
     * @param nums 每间房屋的金额数组
     * @return 能偷到的最大金额
     */
    public int rob(int[] nums) {
        // 边界条件：空数组无法偷窃，返回 0
        if (nums == null || nums.length == 0) return 0;
        int n = nums.length;
        // dp[i] 表示考虑前 i 间房屋（索引 0 到 i-1）能偷到的最大金额
        int[] dp = new int[n + 1];

        // base case：0 间房收益 0；1 间房只能偷它
        dp[0] = 0;
        dp[1] = nums[0];

        for (int i = 2; i <= n; i++) {
            // 第 i 间（索引 i-1）有两种选择：不偷则 dp[i-1]，偷则 dp[i-2]+nums[i-1]（因为不能相邻）
            // 例如：nums=[2,7,9,3,1]，i=3 时 dp[3]=max(dp[2], dp[1]+9)=max(7, 2+9)=11
            dp[i] = Math.max(dp[i - 1], dp[i - 2] + nums[i - 1]);
        }

        return dp[n];
    }
}
